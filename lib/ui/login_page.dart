  Future<void> registerVoid() async{
    _email=signupEmailController.text;
    _password=signupPasswordController.text;

    _confirm=signupConfirmPasswordController.text;

      if(_password==_confirm){
          try{
                await FirebaseAuth.instance.createUserWithEmailAndPassword(email: _email, password: _password);
                
                final docRef = await Firestore.instance.collection('owner').add({                     
                    'email': '$_email ',
                    'password': '$_password',
              
                });
                //INI KUNCINYA BROO!!
                final uid=docRef.documentID.toString();

                await db.collection('databaseLogin').add({
                  'uid': uid,
                  'role': 'admin',
                  'email': '$_email',
                  'password': '$_password'
                });

                _saveUid(uid);   
                
                Navigator.push(
                  context,
                  MaterialPageRoute(builder: (context) => BusinessInfoPage(
                    email: _email,
                    passwordValue: _password,
                    idOwner:uid)
                  )
                );
                // dispose();

          }catch(e){
            print(e.message);
              if(e.code == 'ERROR_EMAIL_ALREADY_IN_USE') {
                  _checkEmail();
              }else if(e.code == 'ERROR_INVALID_EMAIL'){
                  checkFormat();
              }
          }
      }
  }



